/* ShortHand Operation +=, -=, *=, /=, */

let number1 = 17;

// Add Element
number1 += 10;
console.log('add', number1);

// Subtract Element
number1 -= 10;
console.log('subtract', number1);

// Multiply Element
number1 *= 10;
console.log('multiply', number1);
// Division Element
number1 /= 10;
console.log('divisible', parseFloat(number1.toFixed(2)));

// Modulus Element
number1 %= 3;
console.log('Modulus', number1);

// Increment 
number1++;
console.log(number1);
number1--;
console.log(number1);
