/* Math Operation +, -, *, /, % */

const number1 = 17;
const number2 = 3;

// Add Element
const total = number1 + number2;
console.log('total', total);

// Subtract Element
const getback = number1 - number2;
console.log('getback', getback);

// Multiply Element
const multiply = number1 * number2;
console.log('multiply', multiply);

// Division Element
const divisible = number1 / number2;
console.log('divisible', parseFloat(divisible.toFixed(2)));

// Modulus Element

const modulus = number1 % number2;
console.log('Modulus', modulus);