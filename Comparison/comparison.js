// >, <, >=, <=, ==, !=, ===, !==,  ---==> Comparision Operator

const number1 = 45;
const number2 = 50;

console.log(number1 > number2); // output ===> false
console.log(number1 < number2); // output ===> true
console.log(number1 >= number2); // output ===> false
console.log(number1 <= number2); // output ===> true
console.log(number1 == number2); // output ===> false
console.log(number1 != number2); // output ===> true
